    private void btnbusca5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbusca5ActionPerformed
        try {
            int qtd_estoque, qtd_atualizada;
            qtd_estoque =  Integer.parseInt(jestoque.getText());
            qtd_atualizada = Integer.parseInt(jqtdnova.getText());
            
            qtd_nova = qtd_estoque + qtd_atualizada;
            ProductsDAO dao = new ProductsDAO();
            
            dao.adicionarEstoque(idproduto, qtd_nova);
            
            JOptionPane.showMessageDialog(null, "Estoque do produto atualizado");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Produto não encontrado no estoque");
        }
    }//GEN-LAST:event_btnbusca5ActionPerformed


    private void btnbusca4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbusca4ActionPerformed
            // Botão pesquisar
        String nome = "%" + jpesquisa.getText() + "%";

        ProductsDAO dao = new ProductsDAO();
        List<Products> lista = dao.listarProdutosPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) tabelaproduto.getModel();
        dados.setNumRows(0);

        for (Products c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),
                c.getFornecedor().getNome(),
            });
        }
    }//GEN-LAST:event_btnbusca4ActionPerformed

